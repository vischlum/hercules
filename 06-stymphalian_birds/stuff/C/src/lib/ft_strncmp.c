#include "../../inc/libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t i;

	i = 0;
	while ((*s1 != '\0' && *s2 != '\0') && i++ < n)
	{
		if (*s1 != *s2)
			return (*(unsigned char *)s1 - *(unsigned char *)s2);
		s1++;
		s2++;
	}
	if (*s1 != '\0' && i < n)
		return (*(unsigned char *)s1);
	if (*s2 != '\0' && i < n)
		return (-*(unsigned char *)s2);
	return (0);
}
