#include "../../inc/libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	if (!s)
		return ((void)NULL);
	while (*s)
		ft_putchar_fd(*s++, fd);
	ft_putchar_fd('\n', fd);
}
